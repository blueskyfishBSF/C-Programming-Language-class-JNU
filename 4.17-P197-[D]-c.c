//(c)执行以下过程，将1到100的素数生成到程序中。
//这个过程称为埃拉托斯特尼筛法。
//步骤1 用1到 100 之间的数字填充数组 num[100]
//步骤2 从数组中的第二个条目开始，将其所有倍数设置为零。
//步骤3 继续处理下一个非零元素并将其所有倍数设置为零。
//步骤4 重复步骤3，直到将所有非零元素的倍数设置为零
//步骤5 在步骤4结束时，数组中剩余的所有非零条目都将是质数，因此打印出这些数字。
#define _CRT_SECURE_NO_WARNINGS 1
#include <stdio.h>

int main() 
{
    int num[101]; // 用于存储1到100之间的数字的数组
    for (int i = 1; i <= 100; ++i) 
    {
        num[i] = i; // 填充数组，包含1到100的数字
    }

    // 埃拉托斯特尼筛法
    for (int i = 2; i <= 100; ++i) 
    {
        if (num[i] != 0) 
        {
            for (int j = 2 * i; j <= 100; j += i)
            {
                num[j] = 0; // 将i的所有倍数设置为零
            }
        }
    }

    // 打印质数
    printf("1到100之间的质数:\n");
    for (int i = 2; i <= 100; ++i)
    {
        if (num[i] != 0) 
        {
            printf("%d ", num[i]);
        }
    }
    printf("\n");

    return 0;
}
